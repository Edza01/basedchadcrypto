<h2>Cryptojacking definition</h2>
<br>
<p>Cryptojacking is the unauthorized use of someone's computer to mine cryptocurrency. It occurs when a website or other online service uses a person's computer to mine cryptocurrency without their knowledge or consent. </p>

<p>Cryptomining code can run in the background on a victim's computer without their knowledge or consent. The victim may not be aware of the mining activity, but they may notice slower performance or delays in the execution of tasks. The cryptomining code can continue to operate while the victim uses their computer normally.</p>


<br>
<h2>How cryptojacking works</h2>
<br>

<li>A website or online service embeds code into their website or application that utilizes the processing power of a visitor's computer to mine cryptocurrency. This code can be activated as soon as the visitor loads the page or application.</li>
<br>
<li>The visitor's computer begins mining cryptocurrency in the background, while the visitor uses their computer normally. They may not be aware that their computer is being used to mine cryptocurrency.</li>
<br>
<li>The mined cryptocurrency is sent to the person or entity controlling the mining operation. They may use the cryptocurrency for their own purposes or sell it on an exchange.</li>
